//--------------------------------------------------------------------------------------
// Class for stateMovement
//--------------------------------------------------------------------------------------
#pragma once
#include "AIBaseState.h"

//--------------------------------------------------------------------------------------
// stateMovement object
// The state that handles movement in the State Machine decision technique
//--------------------------------------------------------------------------------------
class stateMovement : public AIBaseState
{
public:

	//--------------------------------------------------------------------------------------
	// Default Constructor // Weighting 2 behaviours
	//--------------------------------------------------------------------------------------
	stateMovement();

	//--------------------------------------------------------------------------------------
	// Default Destructor
	//--------------------------------------------------------------------------------------
	~stateMovement();

	//--------------------------------------------------------------------------------------
	// Optional function to run.
	//--------------------------------------------------------------------------------------
	void OnEnter();

	//--------------------------------------------------------------------------------------
	// Calculating the force the player needs to move towards or away from the mouse cursor
	// using weighting.
	//
	// Param:
	//		pAgent: A pointer to the agent so we can get and set the players position.
	//		fDeltaTime: DeltaTime keeps time in seconds.
	//--------------------------------------------------------------------------------------
	void OnUpdate(Agent* pAgent, float fDeltaTime);

	//--------------------------------------------------------------------------------------
	// Currently, nothing draws in this class.
	//
	// Param:
	//		pRenderer2D: A pointer to the 2D rendering engine.
	//--------------------------------------------------------------------------------------
	void OnDraw(Renderer2D* renderer2d);

	//--------------------------------------------------------------------------------------
	// Optional function to run.
	//--------------------------------------------------------------------------------------
	void OnExit();
};
